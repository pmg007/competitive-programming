#include <bits/stdc++.h>
using namespace std;
typedef long long int lli;
int main(){
    lli n;cin>>n;
    if(n<10) cout<<(n);
    else if(n>=10&&n<=99) cout<<(9+(n-10+1)*2);
    else if(n>=100&&n<=999) cout<<(9+90*2+(n-100+1)*3);
    else if(n>=1000&&n<=9999) cout<<(9+90*2+900*3+(n-1000+1)*4);
    else if(n>=10000&&n<=99999) cout<<(9+90*2+900*3+9000*4+(n-10000+1)*5);
    else if(n>=100000&&n<=999999) cout<<(9+90*2+900*3+9000*4+90000*5+(n-100000+1)*6);
    else if(n>=1000000&&n<=9999999) cout<<(9+90*2+900*3+9000*4+90000*5+900000*6+(n-1000000+1)*7);
    else if(n>=10000000&&n<=99999999) cout<<(9+90*2+900*3+9000*4+90000*5+900000*6+9000000*7+(n-10000000+1)*8);
    else if(n>=100000000&&n<=999999999) cout<<(9+90*2+900*3+9000*4+90000*5+900000*6+9000000*7+90000000*8+(n-100000000+1)*9);
    else if(n==1000000000) cout<<8888888899;
    return 0;
}

